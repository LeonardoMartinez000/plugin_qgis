    def asignarPermisosAUsuarios(self, host, bd, usuario_admin, contrasena, usuarios, permisos):
        """Asigna permisos a múltiples usuarios en todas las tablas"""
        import psycopg2
        from .permisos import obtenerTablasBaseDatos
        
        resumen = "RESUMEN DE ASIGNACIÓN DE PERMISOS\n"
        resumen += "=" * 60 + "\n\n"
        
        try:
            # Obtener tablas
            tablas = obtenerTablasBaseDatos(host, bd, usuario_admin, contrasena)
            
            resumen += f"📊 Tablas encontradas: {len(tablas)}\n"
            resumen += f"👥 Usuarios a procesar: {len(usuarios)}\n"
            resumen += f"🔐 Permisos a asignar: {', '.join(permisos)}\n\n"
            
            if len(tablas) == 0:
                resumen += "⚠️ No se encontraron tablas en la base de datos\n"
                return resumen
            
            conn = psycopg2.connect(
                host=host, 
                database=bd, 
                user=usuario_admin, 
                password=contrasena, 
                port='5432'
            )
            cursor = conn.cursor()
            
            total_permisos = 0
            errores = 0
            
            for usuario in usuarios:
                resumen += f"\n👤 Procesando usuario: {usuario}\n"
                resumen += "-" * 60 + "\n"
                usuario_permisos = 0
                
                for permiso in permisos:
                    permisos_asignados_permiso = 0
                    
                    for esquema, tabla in tablas:
                        try:
                            grant_query = f'GRANT {permiso} ON TABLE "{esquema}"."{tabla}" TO "{usuario}";'
                            cursor.execute(grant_query)
                            usuario_permisos += 1
                            permisos_asignados_permiso += 1
                            total_permisos += 1
                        except psycopg2.Error as e:
                            errores += 1
                            # Solo mostrar errores críticos
                            if "already" not in str(e).lower():
                                resumen += f"   ⚠️ Error en {esquema}.{tabla} ({permiso})\n"
                        except Exception as e:
                            errores += 1
                    
                    if permisos_asignados_permiso > 0:
                        resumen += f"   ✓ {permiso}: {permisos_asignados_permiso} tablas\n"
                
                resumen += f"   📈 Total para {usuario}: {usuario_permisos} permisos\n"
            
            conn.commit()
            cursor.close()
            conn.close()
            
            resumen += "\n" + "=" * 60 + "\n"
            resumen += f"✅ PROCESO COMPLETADO\n"
            resumen += f"   • Total de permisos asignados: {total_permisos}\n"
            resumen += f"   • Usuarios procesados: {len(usuarios)}\n"
            resumen += f"   • Tablas procesadas: {len(tablas)}\n"
            resumen += f"   • Permisos aplicados: {', '.join(permisos)}\n"
            if errores > 0:
                resumen += f"   • Errores encontrados: {errores}\n"
            
            return resumen
            
        except Exception as e:
            raise Exception(f"Error al asignar permisos: {str(e)}")
    
    def validarPermisos(self):
        """Obtiene y muestra los usuarios con permisos en la BD"""
        self.mostrarProceso.setText('Obteniendo usuarios y permisos...')
        QApplication.processEvents()
        
        bd, host, usuario, contrasena = self.credenciales()
        
        try:
            usuarios_permisos = obtenerUsuariosYPermisos(host, bd, usuario, contrasena)
            
            # Limpiar lista anterior
            self.listUsuariosPermisos.clear()
            self.usuarios_permisos_dict = {}
            
            # Agregar usuarios a la lista con checkboxes
            for user, permisos in usuarios_permisos:
                item = QListWidgetItem(user)
                item.setFlags(item.flags() | Qt.ItemIsUserCheckable)
                item.setCheckState(Qt.Checked)
                item.setData(Qt.UserRole, user)
                self.listUsuariosPermisos.addItem(item)
                self.usuarios_permisos_dict[user] = permisos
            
            self.mostrarProceso.setText(f'Se encontraron {len(usuarios_permisos)} usuarios con permisos.')
            self.mensaje('Ejecutado', f'Se encontraron {len(usuarios_permisos)} usuarios')
            
        except Exception as e:
            self.mostrarProceso.setText(f'Error: {str(e)}')
            self.mensaje('Error', str(e))
    
    def mostrarPermisosUsuario(self):
        """Muestra los permisos del usuario seleccionado"""
        from PyQt5.QtWidgets import QTableWidgetItem
        
        items_seleccionados = self.listUsuariosPermisos.selectedItems()
        
        if not items_seleccionados:
            self.permisosGroupBox.setTitle("Selecciona un usuario para ver/editar permisos")
            self.checkSELECT.setChecked(False)
            self.checkINSERT.setChecked(False)
            self.checkUPDATE.setChecked(False)
            self.checkDELETE.setChecked(False)
            self.tablasPermisosTable.setRowCount(0)
            return
        
        usuario = items_seleccionados[0].data(Qt.UserRole)
        self.usuario_actual = usuario
        
        bd, host, usuario_admin, contrasena = self.credenciales()
        
        try:
            # Obtener permisos detallados del usuario
            permisos_detallados = obtenerPermisosDetallados(host, bd, usuario_admin, contrasena, usuario)
            
            self.permisos_usuario_actual = permisos_detallados
            
            # Actualizar checkboxes
            self.checkSELECT.setChecked(permisos_detallados.get('SELECT', False))
            self.checkINSERT.setChecked(permisos_detallados.get('INSERT', False))
            self.checkUPDATE.setChecked(permisos_detallados.get('UPDATE', False))
            self.checkDELETE.setChecked(permisos_detallados.get('DELETE', False))
            
            # Llenar tabla con permisos por tabla
            tablas = permisos_detallados.get('tablas', [])
            self.tablasPermisosTable.setRowCount(len(tablas))
            
            for row, tabla_info in enumerate(tablas):
                # Validar que tabla_info tenga suficientes elementos
                if len(tabla_info) < 6:
                    continue
                    
                esquema = tabla_info[0] if tabla_info[0] else ""
                tabla = tabla_info[1] if tabla_info[1] else ""
                tiene_select = tabla_info[2] if tabla_info[2] is not None else False
                tiene_insert = tabla_info[3] if tabla_info[3] is not None else False
                tiene_update = tabla_info[4] if tabla_info[4] is not None else False
                tiene_delete = tabla_info[5] if tabla_info[5] is not None else False
                
                # Esquema
                item_esquema = QTableWidgetItem(str(esquema))
                item_esquema.setFlags(item_esquema.flags() & ~Qt.ItemIsEditable)
                self.tablasPermisosTable.setItem(row, 0, item_esquema)
                
                # Tabla
                item_tabla = QTableWidgetItem(str(tabla))
                item_tabla.setFlags(item_tabla.flags() & ~Qt.ItemIsEditable)
                self.tablasPermisosTable.setItem(row, 1, item_tabla)
                
                # SELECT
                item_select = QTableWidgetItem("✓" if tiene_select else "")
                item_select.setFlags(item_select.flags() & ~Qt.ItemIsEditable)
                self.tablasPermisosTable.setItem(row, 2, item_select)
                
                # INSERT
                item_insert = QTableWidgetItem("✓" if tiene_insert else "")
                item_insert.setFlags(item_insert.flags() & ~Qt.ItemIsEditable)
                self.tablasPermisosTable.setItem(row, 3, item_insert)
                
                # UPDATE
                item_update = QTableWidgetItem("✓" if tiene_update else "")
                item_update.setFlags(item_update.flags() & ~Qt.ItemIsEditable)
                self.tablasPermisosTable.setItem(row, 4, item_update)
                
                # DELETE
                item_delete = QTableWidgetItem("✓" if tiene_delete else "")
                item_delete.setFlags(item_delete.flags() & ~Qt.ItemIsEditable)
                self.tablasPermisosTable.setItem(row, 5, item_delete)
            
            self.permisosGroupBox.setTitle(f"Permisos para: {usuario}")
            
        except Exception as e:
            self.mensaje('Error', f'Error al obtener permisos: {str(e)}')
    
