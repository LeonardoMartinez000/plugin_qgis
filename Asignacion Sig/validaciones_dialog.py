    def aplicarPermisosUsuario(self, host, bd, usuario_admin, contrasena, usuario_objetivo, permisos_activos, tablas):
        """Aplica los permisos seleccionados al usuario"""
        import psycopg2
        
        try:
            conn = psycopg2.connect(
                host=host, 
                database=bd, 
                user=usuario_admin, 
                password=contrasena, 
                port='5432'
            )
            cursor = conn.cursor()
            
            # Primero revocar todos los permisos
            for esquema, tabla, _, _, _, _ in tablas:
                try:
                    cursor.execute(f'REVOKE ALL ON TABLE "{esquema}"."{tabla}" FROM "{usuario_objetivo}";')
                except:
                    pass
            
            # Luego otorgar solo los permisos seleccionados
            for permiso in permisos_activos:
                for esquema, tabla, _, _, _, _ in tablas:
                    try:
                        cursor.execute(f'GRANT {permiso} ON TABLE "{esquema}"."{tabla}" TO "{usuario_objetivo}";')
                    except:
                        pass
            
            conn.commit()
            cursor.close()
            conn.close()
            
        except Exception as e:
            raise Exception(f"Error al aplicar permisos: {str(e)}")
    
